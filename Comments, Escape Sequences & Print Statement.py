# Escape Sequences;

#1. \n (New Line)
print("Hello\nWorld")

#2. \t (New Tab)
print("Hello\tWorld")

#3. \000 (Octal Value)
print("\110\145\154\154\157")

#4. \xhh (Hex Value)
print("\x48\x65\x6c\x6c\x6f")

#5. \\ (Backslash)
print("Hello\\")

#6. \' (Single Quote)
print("Suman\'s Phone")


# Print Statement;

#1 Printing a single statement
print("Hello World")

#2 Printing 2 or more statements
print("Hello World")
print("Good Morning")

#3 If Statement needs to be within same line
print("Hello World", end=" ")
print("Good Morning")